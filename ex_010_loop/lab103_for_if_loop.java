package ex_010_loop;

public class lab103_for_if_loop {
    public static void main(String[] args) {
        for (int shruti = 0; shruti < 18; shruti++) {
            // 0 to 17, 18 times
            if (shruti > 15) {
                System.out.println("Gift From Papa : iphone");
            } else {
                System.out.println("She will get only hugs! or Candy!");
            }
        }
    }
}