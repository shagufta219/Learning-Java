package ex_02_operators;

public class operator_relational {
    public static void main(String[] args) {

                // < Less Than
                // < =  -> Less than or equal to
                // > Greater
                // > = Greater or equal
                // == ->  Equal to (but checking)
                // != -> Not equal

                // All of them will result boolean output.

                int a = 10;
                int b = 30;
                boolean c = a > b; // 10 >30
                System.out.println(c);

                int age_saba = 21;
                int age_raza = 29;
                boolean result = age_raza >= age_saba;
                System.out.println(result);

                // age_raza > age_saba or age_raza = age_saba
            }
        }
